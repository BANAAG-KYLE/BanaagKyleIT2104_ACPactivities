set1 = {8, 16, 24, 32, 44}
set2 = {7, 14, 8, 32, 21}

print("Set Difference:")
print("set1 - set2:", set1 - set2)
print("set2 - set1:", set2 - set1)

print("Union of Sets:")
print("set1 | set2:", set1 | set2)

print("Symmetric Difference:")
print("set2 ^ set1:", set2 ^ set1)
print("set1 ^ set2:", set1 ^ set2)

print("Set Intersection:")
print("set1 & set2:", set1 & set2)
print("set2 & set1:", set2 & set1)